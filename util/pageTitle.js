export const getPageTitle = (pathName) => {
    switch (pathName) {
        case '/':
            return 'Trang chủ';
        case '/ds-tinh-thanh':
            return 'Danh mục Tỉnh/ Thành Phố';
        case '/ds-chung-thu':
            return 'Danh Sách Chứng thư';
        case '/ds-bc-tham-dinh':
            return 'Danh Sách Báo Cáo Thẩm Định';
        case '/ds-chi-tieu-bao-cao':
            return 'Danh Sách Chỉ Tiêu Báo Cáo';
        case '/ds-phieu-tam-thu':
            return 'Danh Sách Phiếu Tạm Thu';
        case '/ds-phieu-yeu-cau':
            return 'Quản lý phiếu yêu cầu';
        case '/phieu-yeu-cau':
            return 'Phiếu Yêu Cầu';
        case '/phieu-tam-thu':
            return 'Phiếu Tạm Thu';
        case '/thong-bao':
            return 'Thông báo';
        case '/tuy-chinh':
            return 'Tùy Chỉnh';
        case '/post':
            return 'Posts';
        case '/bao-cao':
            return 'Báo Cáo';
        case '/bao-cao-tham-dinh':
            return 'Báo Cáo Thẩm Định';
        case '/bao-cao-chung-thu':
            return 'Báo Cáo Chứng Thư';
        case '/ds-tinh-thanh':
            return 'Danh mục Tỉnh/ Thành Phố';
        case '/ca-nhan':
            return 'Cá Nhân';
        case '/ds-luong-kinh-doanh':
            return 'Danh sách lương kinh doanh';
        case '/ds-phieu-chi':
            return 'Danh sách Phiếu chi';
        default:
            return '';
    }
};
